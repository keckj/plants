/**
 * Fisher-Yates shuffle (in-place).
 * Returns a new shuffled array (does not mutate the original).
 */
export function shuffle(array) {
  const a = [...array]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[a[i], a[j]] = [a[j], a[i]]
  }
  return a
}
